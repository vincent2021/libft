#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int n;

	n = 0;
	while (*s)
	{
		s++;
		n++;
	}
	while (n-- && *s != c)
		s--;
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
